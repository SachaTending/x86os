void kbd_int(registers_t *regs) {
    uint8_t scancode = inb(0x60);
    char c = kbdus[scancode];
    if (scancode & 0x80) {
        return;
    } else {
        printf("%c", c);
        if (c == '\n') {
            // Handle commands
            if (!strcmp("vesa", (char *)&buf)) {
                vesa_test();
            } else if (!strcmp("memmap", (char *)&buf)) {
                memmap_print();
            } else if (!strcmp("set", (char *)&buf)) {
                vesa_set();
            } else if (!strcmp("info", (char *)&buf)) {
                vesa_info();
            }
            cls_buff();
        } else {
            buf[buf_loc++] = c;
        }
    }
}

