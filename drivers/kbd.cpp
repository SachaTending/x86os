#define font_height 16
void gui_putchar(char c) {
    if (!in_vesa) return;
    if (c == '\n') {
        screen_y += font_height;
        screen_x = 0;
        goto end;
    }
    if (screen_x > mode_info->height) {
        screen_y += font_height;
        screen_x = 0;
    }
    if (screen_y > mode_info->width) {
        screen_x = 0;
        screen_y = 0;
    }
    //draw_text(&c, screen_x, screen_y);
    drawchar(c, screen_x, screen_y, 0xffffffff, 0x0);
    screen_x += font_height;
end:
    if (screen_x > mode_info->height) {
        screen_y += font_height;
        screen_x = 0;
    }
    if (screen_y > mode_info->width) {
        screen_x = 0;
        screen_y = 0;
    }
}

